    private void carregarTabela(){
        String[] colunas = {"Nome", "Tipo" , "Escala"};
        
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers( colunas );
        
        for( Carrinhos carr : listaDeCarrinhos){
            Object[] linha = { carr.nome , carr.tipo , carr.escala};
            tableModel.addRow( linha );
        }
        
        TabeladeCarrinhos.setModel( tableModel );
        
    }

