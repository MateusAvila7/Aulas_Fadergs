    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       
        String nome = txtNome.getText();
        if( nome.isEmpty() || cmbEscala.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "O nome e a escala são necesarias");
        }else{
            Carrinhos carr = new Carrinhos();
            carr.nome = nome;
            carr.tipo = cmbTipo.getSelectedItem().toString();
            carr.escala = cmbEscala.getSelectedItem().toString();
            
            listaDeCarrinhos.add(carr);
            
            carregarTabela();
            
            txtNome.setText("");
            cmbTipo.setSelectedIndex(0);
            cmbEscala.setSelectedIndex(0);
           
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

